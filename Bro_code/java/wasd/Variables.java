package learn.Bro_code.java.wasd;

public class Variables {
    public static void main(String[] args) {
        int a; // declaration
        a = 2000000000; // assignment

        float b = 3.141592F; // initialization
        // for floats you have to use F
        // floats are for decimals

        double c = 3.141592653589793; // doubles are like floats but take take more numbers

        long d = 1234567890L; // for longs you have to put L at the end
        // longs are like ints but take for intger

        boolean e = true; // boolean only take true or false

        short f = 123; // take less then int

        byte g = 123; // takes less then small

        char h = '@'; // can take any one charatcer but has to be in ''

        String i = "wasd"; // can take as many as you want but has to be in ""

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
    }
}
